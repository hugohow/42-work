int	is_skippable(char c)
{
	if (c >= '0' && c <= '9')
		return (0);
	if (c == '-' || c == '/' || c == '*' || c == '%' || c == '(')
		return (0);
	return (1);
}

int	is_operator(char c)
{
	if (c == '/' || c == '*' || c == '%' || c == '-')
		return (1);
	return (0);
}
