void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	while (*str)
	{
		ft_putchar(*str);
		str++;
	}
}

int		main(int argc, char **argv)
{
	int i;

	if (argc == 1)
		return (0);
	i = 1;
	while (i < argc)
	{
		ft_putstr(argv + i);
		i++;
	}
}
